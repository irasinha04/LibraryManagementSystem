package com.gitlab.irasinha04.jlm;

public class Member {
	private int ID;
	private String Name;
	private String JoinDate;

//Getters and Setters
	public int getID() {
		return ID;
	}

	public void setID(int id) {
		ID = id;
	}

	public String getJoinDate() {
		return JoinDate;
	}

	public void setJoinDate(String joinDate) {
		JoinDate = joinDate;
	}


	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}
}
